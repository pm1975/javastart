public class TimeConverter {
    int hoursToMinutes(int hour) {
        return hour * 60;
    }

    int minutesToSeconds(int minutes) {
        return minutes * 60;
    }

    int secondsToMiliseconds(int seconds) {
        return seconds * 1000;
    }
}
