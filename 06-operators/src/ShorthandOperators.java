public class ShorthandOperators {
    //OPERATORY SKRÃ“CONE
    public static void main(String[] args) {
        int x = 10;
        x += 5;
        System.out.println(x); //15
        x *= 3;
        System.out.println(x); //45
        x /= 5; //9
        System.out.println(x);
        x -= 2; //7
        System.out.println(x);
        x %=4; //3
        System.out.println(x);
    }

}
